                Health
            </h3>
            <p class="paragraph_dog_cat_care">
                Your cat should see the veterinarian at least once a year for an examination and annual shots, and
                immediately if she is sick or injured.
            </p>
            <h3 class="header_3_dog_cat_care">
                Scratching
            </h3>
            <p class="paragraph_dog_cat_care">
                Cats need to scratch! When a cat scratches, the old outer nail sheath is pulled off and the sharp,
                smooth claws underneath are exposed. Cutting your cat's nails every two to three weeks will keep them
                relatively blunt and less likely to harm the arms of both humans and furniture. Provide your cat with a
                sturdy scratching post, at least three feet high. The post should also be stable enough that it won't
                wobble during use, and should be covered with rough material such as sisal, burlap or tree bark. Many
                cats also like scratching pads.
            </p>
