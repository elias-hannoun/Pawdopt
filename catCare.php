            <h3 class="header_3_dog_cat_care">
                Grooming
            </h3>
            <p class="paragraph_dog_cat_care">
                Most cats stay relatively clean and rarely need a bath, but you should brush or comb your cat regularly.
                Frequent brushing helps keep your cat's coat clean, reduces the amount of shedding and cuts down on the
                incidence of hairballs.
            </p>
            <h3 class="header_3_dog_cat_care">
                Housing
            </h3>
            <p class="paragraph_dog_cat_care">
                Your pet should have her own clean, dry place in your home to sleep and rest. Line your cat's bed with a
                soft, warm blanket or towel. Be sure to wash the bedding often. Please keep your cat indoors. Outdoor
                cats do not live as long as indoor cats. Outdoor cats are at risk of trauma from cars, or from fights
                with other cats, raccoons and free-roaming dogs. Coyotes are known to eat cats. Outdoor cats are more
                likely to become infested with fleas or ticks, as well as contract infectious diseases.
            </p>
            <h3 class="header_3_dog_cat_care">
                Identification
            </h3>
            <p class="paragraph_dog_cat_care">
                If allowed outdoors, your cat must wear a safety collar and an ID tag. A safety collar with an elastic
                panel will allow your cat to break loose if the collar gets caught on something. And for both indoor and
                outdoor cats, an ID tag or an implanted microchip can help ensure that your cat is returned if he or she
                becomes lost.
            </p>
